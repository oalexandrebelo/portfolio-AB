function OrbitRing({
  tools,
  size,
  duration,
  reverse = false,
}: {
  tools: { name: string; icon: () => React.JSX.Element }[];
  size: number;
  duration: number;
  reverse?: boolean;
}) {
  const radius = size / 2;
  const angleStep = 360 / tools.length;

  return (
    <div
      className={`absolute top-1/2 left-1/2 z-10 -translate-x-1/2 -translate-y-1/2 rounded-full ${
        reverse ? "animate-counter-orbit" : "animate-orbit"
      }`}
      style={{ width: size, height: size, "--duration": `${duration}s` } as React.CSSProperties}
    >
      <div className="relative h-full w-full">
        {tools.map((tool, i) => {
          const angle = angleStep * i;
          const Icon = tool.icon;
          return (
            <div
              key={tool.name}
              className="absolute top-1/2 left-1/2 -translate-x-1/2 -translate-y-1/2"
              style={{ transform: `rotate(${angle}deg) translateX(${radius}px)` }}
            >
              <div style={{ transform: `rotate(-${angle}deg)` }}>
                <div
                  className={`shadow-aceternity flex size-14 items-center justify-center rounded-xl bg-card border border-border/50 ${
                    reverse ? "animate-orbit" : "animate-counter-orbit"
                  }`}
                  style={{ "--duration": `${duration}s` } as React.CSSProperties}
                  title={tool.name}
                >
                  <Icon />
                </div>
              </div>
            </div>
          );
        })}
      </div>
    </div>
  );
}

